      <h4>Data Challenge</h4>
      <p>Que ce soit durant le festival ou tout au long de l’année, la recette du Data Challenge reste la même. Des étudiants motivés, des problématiques concrètes, des entreprises impliquées. </p>
    </div>

    <div class="carte" id="definition-dataBattle">
    <h4>Data Battle</h4>
      <p> Une data battle est une compétition ou un défi où des équipes s'affrontent pour analyser, interpréter et tirer des enseignements à partir de données fournies. Les participants utilisent leurs compétences en traitement de données, en analyse statistique et en visualisation pour proposer des solutions ou des insights pertinents. Les data battles favorisent l'échange de connaissances et stimulent l'innovation dans le domaine de l'analyse de données.</p>
