    <h4>IA Pau</h4>
    <p> Dans une atmosphère conviviale, l'association IA PAU réunit des chercheurs, des enseignants, des étudiants, des entrepreneurs et des particuliers intéressés par le traitement des données. Son but est de créer des liens entre le monde économique, le milieu académique et le grand public en organisant des événements et des projets collaboratifs.</p>

    <p> L'objectif principal de cette association est de rendre accessibles et de partager les connaissances liées à l'avancement majeur du XXIe siècle qu'est l'intelligence artificielle. Cette avancée suscite de nombreuses interrogations sur les plans scientifique, technologique, éthique et démocratique. </p>
  </div>

